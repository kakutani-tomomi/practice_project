package basic.q06;

public class ShowSeasons {

	public static void main(String[] args) {
		int season = 1;

		switch (season) {
		//冬
		case 1:
			System.out.println("1月は冬です。");
			break;
		case 2:
			System.out.println("2月は冬です。");
			break;
		//春	
		case 3:
			System.out.println("3月は春です。");
			break;
		case 4:
			System.out.println("4月は春です。");
			break;
		case 5:
			System.out.println("5月は春です。");
			break;
		//夏	
		case 6:
			System.out.println("6月は夏です。");
			break;
		case 7:
			System.out.println("7月は夏です。");
			break;
		case 8:
			System.out.println("8月は夏です。");
			break;
		//秋
		case 9:
			System.out.println("9月は秋です。");
			break;
		case 10:
			System.out.println("10月は秋です。");
			break;
		case 11:
			System.out.println("11月は秋です。");
			break;
		//冬
		case 12:
			System.out.println("12月は冬です。");
			break;
		//13月
		case 13:
			System.out.println("13月は存在しないです。");
			break;
		}

	}

}
